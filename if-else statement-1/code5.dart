// write a program to check if a character is a vowel or consonant

void main() {
  String ch = "N";
  if (ch == "A" ||
      ch == "E" ||
      ch == "I" ||
      ch == "O" ||
      ch == "U" ||
      ch == "a" ||
      ch == "e" ||
      ch == "i" ||
      ch == "o" ||
      ch == "u") {
    print("Vowel");
  } else {
    print("Consonant");
  }
}
